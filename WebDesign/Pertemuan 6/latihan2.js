// Arithmetic Operators
let sum = 10 + 5; //addition
let difference = 10 - 5; //subtraction
let product = 10 * 5; //multiplication
let quotient = 10 / 5; //division
let reminder = 13 % 2; //modulo
let exponent = 10 ** 5; //exponent
console.log(sum, difference, product, quotient, reminder, exponent);

// Comparison Operators
let isEqual = 5 === '5'; // false (strict equality)
let isEqual2 = 5 == "5"; // Not strict
let isNotEqual = 10 != '10'; // false
let isNotEqual2 = 10 !== "10"; //
let isGreaterThan = 15 > 10; // true
console.log(isEqual, isEqual2, isNotEqual, isNotEqual2, isGreaterThan);

// Logical Operator
let isBothTrue = true && true; // true
let isBothTrue2 = true && false; // false
let isEitherTrue = true || false; // true
let isEitherTrue2 = true || true; // true
let isEitherTrue3 = false || false; // false
let isNotTrue = !true; // false
let isNotFalse = !false; // true
console.log(isBothTrue, isBothTrue2, isEitherTrue, isEitherTrue2, isEitherTrue3, isNotTrue, isNotFalse);